    private void btn_regActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_regActionPerformed
        // TODO add your handling code here:
        try {
            String detailPaket = "";
            String detailResi ="";
            String nama = input_user_reg.getText();
            String ID = input_id.getText();
            int nohp = Integer.parseInt(input_nohp.getText());
            //            String password = String.valueOf(input_pass_reg.getPassword());
            //            String confirmPassword = String.valueOf(input_conf_reg.getPassword());

            if (nama.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Username tidak boleh kosong");
                this.requestFocus();
            }
            else {
                ctrl.registerUserPengirim(detailPaket, detailResi,ID, nama, nohp);
                this.dispose();
                LoginViewPengirim logView = new LoginViewPengirim();
                logView.show();
            }
        } catch (Exception e) {
            System.out.println("Register Gagal");
        }
    }//GEN-LAST:event_btn_regActionPerformed

